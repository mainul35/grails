class DatabaseInfo {

    String name
    String username
    String password
    String hostname

    String getName() {
        return name
    }

    void setName(String name) {
        this.name = name
    }

    String getUsername() {
        return username
    }

    void setUsername(String username) {
        this.username = username
    }

    String getPassword() {
        return password
    }

    void setPassword(String password) {
        this.password = password
    }

    String getHostname() {
        return hostname
    }

    void setHostname(String hostname) {
        this.hostname = hostname
    }


}
